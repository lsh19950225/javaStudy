package days04;

public class Ex04 {

	public static void main(String[] args) {
		// 쉬프트 연산자 >> <<
		//	: 비트 연산자
		//	: 물건을 이동시키다.
		System.out.println(10);
		System.out.println(Integer.toBinaryString(10)); // 2진수
		System.out.println(Integer.toOctalString(10)); // 8진수
		System.out.println(Integer.toHexString(10)); // 16진수
		

	}

}
