package days02;

public class Ex04 {
    // main() 메서드의 매개변수인 String[] args도 지역변수이다.
	public static void main(String[] args) {
	
		// 변수 범위 p68
		// 문자열 타입 String %s
		// 숫자(실수) float(4), double(8) (메모리크기) %f
		// 숫자(정수) int(4) %d
		
		// 나이를 저장하는 age 변수 선언
		// {} 범위(영역) 연산자
		
		int age ;
		
		// age cannot be resolved to a variable
		 age = 20;
		
		System.out.printf("> age = %d\n", age);

	}

}
