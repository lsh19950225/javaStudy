public class Ex03 {

	public static void main(String[] args) {
		// println() 메서드 - 출력
		System.out.println("이시훈");
		System.out.println("송세호");
		System.out.println("이창익");
		
		// print() 메서드 - 출력
		System.out.print("이시훈"+'\n');
		System.out.print("송세호"+'\r');
		System.out.print("이창익\n");
		System.out.println(); // 개행
		System.out.println("김준석");
		
	    // 줄바꿈(개행) 여부 == CR \r + LF \n
		
	}

}
